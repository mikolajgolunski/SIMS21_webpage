              <h2>How to get to Krak&oacute;w?</h2>
              <h3>By air</h3>
              <p><a href="http://www.krakowairport.pl/en" target="_blank">The John Paul II International and Domestic Airport Krak&oacute;w Balice</a> is located at the outskirts of the city. The fastest way to get from the airport to the city center (the journey takes about 17 minutes) is the shuttle train</a> service, which links the airport with the Krak&oacute;w's Main Railway Station located in the city center. The trains run <strike>every 30&nbsp;minutes</strike> between 4:00 (4:00&nbsp;a.m.) and midnight. <span class="important">From 3 September to 14 October trains will run every 60 minutes due to renovation works. Additional bus communication will be introduced between train courses.</span> Current Airport-Krakow train timetable is in <a href="./resources/rozklad_pociagow_lotnisko_krakow.pdf" download>here</a>. &quot;normal&quot; means a train. Empty slot means a bus. A single ticket costs 9&nbsp;PLN. It can be purchased on board. Luggage is free of charge. The Krak&oacute;w Airport train station is located near the passenger terminal, at the rear of the multi-storey car park. Trains are equipped with air conditioning, power outlets, Wi-Fi and ticket machines. All trains also have spaces for disabled passengers and persons with reduced mobility.</p>
