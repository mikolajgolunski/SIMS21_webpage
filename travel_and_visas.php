              <h2>Practical information</h2>
              <h3>People</h3>
              <p>The population of Poland is 38&nbsp;million. Over 1.8&nbsp;million live in Warsaw - the capital of Poland. Polish is the official language. English and German are among the better-known foreign languages. The population is largely of Polish descent and more than 90% profess to be Roman Catholics.</p>

              <h3>Currency</h3>
              <p>The official Polish unit of currency is Z&#322;oty&nbsp;(PLN). Major convertible foreign currencies may be changed into Polish z&#322;oty at banks, hotels and at private currency-exchange offices (in Polish &quot;kantor&quot;). In general, traveler's checks can be cashed and all major credit cards are accepted. The current exchange rate can be found <a href="http://www.ratesfx.com/rates/rate-pln.html" target="_blank">here</a>. There are many ATM machines located in vicinity of banks. They accept MasterCard and Visa cards.</p>

              <h3>Climate</h3>
              <p>Poland lies in the zone of moderate climate, intermediate between the marine and continental climates and the weather can be rather changeable. For current information, see <a href="http://www.weather.com/weather/today/l/PLXX0012:1:PL" target="_blank">Forecast</a>. There is usually a good warm weather in Poland in September. The temperature can be very nice, ranging from 18 till 25&nbsp;&deg;C.</p>

              <h2>More practical information for participants</h2>
              <h3>Public transport in Krak&oacute;w</h3>
              <p>Getting around in Krak&oacute;w is pretty easy. Krak&oacute;w has a fairly dense public transport system which consists of tramways and bus lines.</p>

              <p>One-way ticket for a municipal bus or a streetcar (tram) is 3.8&nbsp;z&#322;oty (an equivalent of roughly 0.9&nbsp;Euro). You can buy it at newsstands and from ticket machines at downtown stops and in some buses and tram cars. If there is a ticket machine in bus or tram it is located at the front part of the bus or in the first cart of the tram. Drivers sell 60-minute tickets worth five z&#322;oty. Immediately after boarding you should put the ticket through the ticket puncher and keep it till you reach your destination.</p>

              <p>A 20-minutes ticket allowing for changing lines costs 2.8&nbsp;z&#322;oty (PLN), its 40-minutes equivalent is 3.8&nbsp;z&#322;oty (in fact it is the same one as a one-way ticket, you can use at either as a one-way ticket with no time limit or as a 40-minute ticket while changing lanes), an hourly one 5&nbsp;z&#322;oty, and 90-minute ticket has been priced at 6&nbsp;z&#322;oty. The prices of unlimited-travel passes for all municipal buses and streetcars have been set at 15&nbsp;z&#322;oty for a 24-hour ticket, at 24&nbsp;z&#322;oty for a 48-hour one, and at 36&nbsp;z&#322;oty for a 72-hour pass, while a seven-day unlimited-travel ticket costs 48&nbsp;z&#322;oty. Tickets are valid for the stated period starting with their first punching on a bus or a tram (don't punch your ticket any more till its expiration).</p>

              <p>All of the above-mentioned tickets are good for municipal bus services within the city limits only. A one-way ticket for buses going beyond the administrative boundaries of Krak&oacute;w - bus numbers from 200 to 304 - costs 4&nbsp;z&#322;oty (so-called &quot;bilet aglomeracyjny&quot;).</p>

