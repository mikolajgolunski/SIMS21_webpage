              <h2>Auschwitz-Birkenau</h2>
              <p class="important">Tuesday, September&nbsp;12, 2017</p>
              
              <p>
                <span class="fa-stack fa-1x">
                  <span class="fa fa-square-o fa-stack-2x"></span>
