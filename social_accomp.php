              <p>The tour of the Museum, included on the UNESCO World Heritage List in 1978, comprises both parts of the former concentration camp - Auschwitz I and Auschwitz II-Birkenau - and a documentary film presenting the first moments after the camp was liberated. The Auschwitz camp has become a symbol of terror, genocide and the Holocaust. It was established by the Nazis in 1940 in the suburbs of the city of Auschwitz, which was integrated into the Third Reich. The direct reason for the establishment of the camp was the growing number of mass arrests of Poles and overcrowding of the existing prisons. Initially, it was supposed to be another concentration camp created by the Nazis from the beginning of the 1930s. That was the function of Auschwitz for the entire period of its existence, even when, from 1942, it became one of the centres for &quot;Endl&#246;sung der Judenfrage&quot; (&quot;the final solution of the Jewish Question&quot;) at the same time - the Nazi plan to murder the Jews living in the territories occupied by the Third Reich.</p>

              <p>The Auschwitz camp, until the end of its existence, was first and foremost a place of extermination. Even if from 1943 mortality was reduced in other camps to conserve manpower, in Auschwitz, to which continually new shipments were coming - mostly of Jews - to supply the camp labor, human life never meant too much. Historians estimate that in less than five years of the camp's existence 1 - 1.5&nbsp;million people were killed in Auschwitz, most of them - approximately 1 - 1.35&nbsp;million - were Jews. The second most numerous group were Poles (approx. 70&nbsp;000 - 75&nbsp;000), the third - Gypsies (approx. 20&nbsp;000). Approximately 15&nbsp;000 Soviet prisoners of war and 10&nbsp;000 - 15&nbsp;000 prisoners of other nationalities (including Czechs, Belarusians, Yugoslavs, French, Germans and Austrians) were also killed in the camp.</p>

              <p>Due to the role that Auschwitz played in the implementation of the Nazi extermination plans, it has become a world-famous symbol of the Nazi genocide, in particular the destruction of the Jews. In the Auschwitz I camp the Nazis established the first camp for men and women; it was the place where the first experiments of killing using Zyklon B took place, the first mass transports of Jews were being murdered, the first criminal experiments on prisoners were conducted, and the majority of executions by shooting were performed. It was there, in Block 11, where the central detention camp for prisoners from all parts of the camp was housed, as well as the main camp commandant office and most other SS offices. The camp authorities supervised further expansion of the camp from there.</p>

