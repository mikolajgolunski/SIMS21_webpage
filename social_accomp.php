              <p>This, undoubtedly one of the Poland's prime tourist attractions, gives you a chance to admire some of the most amazing scenery in the Polish highlands. Usually referred to as &quot;the raft trip down the Dunajec river&quot;, this is in fact a rather leisurely drift in large wooden punts. Highly experienced guides make sure you have a smooth ride and keep you informed about the sights you see and the history of the trips, which goes back to 1840s. The voyage down the winding Dunajec offers breathtaking views of mighty limestone cliffs plunging into the water as well as stunning native vegetation both protected by the second oldest international national park in the world. Also, while sailing down the river you can spot some rare bird species including the black stork. Another attraction of the tour is a visit to Niedzica Castle which was originally built in the 14<sup>th</sup>&nbsp;century by the Hungarians to protect an important trade through the Dunajec valley. Nowadays the castle towers over a large water reservoir and an impressive dam built in mid 90s. While visiting the castle you can admire the delightful view over the artificial lake and learn an amazing story of Inca refugees that found shelter there. Our trip begins at Niedzica Castle, then continues to the wharf in Sromowce Ni&#380;ne and ends 15&nbsp;km later in Szczawnica. The voyage lasts two to three hours, depending on the water level of the river. It is advisable to bring rainwear or a sun hat depending on the weather.</p>

