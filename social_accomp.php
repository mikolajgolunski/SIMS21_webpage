              <p>For many centuries Jews played their part in creating the history and culture of the city of Kroke, as Krak&oacute;w is known in Yiddish. Before World War II they made up as much as 25% of the city's population. A tour around Kazimierz - the former Jewish district, preserved in excellent condition until today - will introduce you to the world of their rich culture, customs and history. It was there that the renowned philosopher Moj&#380;esz Isserles (called Remuh) taught, Helena Rubinstein was born, who is considered to have been one of the richest women in the world, and the esteemed director, actor and screenwriter Roman Pola&#324;ski spent his childhood. The old Jewish district, included on the UNESCO World Heritage List in 1978, became an even more recognizable landmark in Europe following the release of the famous &quot;Schindler's List&quot;.</p>

